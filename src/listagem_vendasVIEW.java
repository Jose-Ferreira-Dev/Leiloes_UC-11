     private void listarProdutos(){
    try {
        ProdutosDAO produtosdao = new ProdutosDAO();
        
        DefaultTableModel model = (DefaultTableModel) listaProdutos2.getModel();
        model.setNumRows(0);
        
        ArrayList<ProdutosDTO> listagem = produtosdao.listarProdutos();
        
        for(int i = 0; i < listagem.size(); i++){
            // Verifica se o status do produto Ã© "Vendido"
            if ("Vendido".equals(listagem.get(i).getStatus())) {
                model.addRow(new Object[]{
                    listagem.get(i).getId(),
                    listagem.get(i).getNome(),
                    listagem.get(i).getValor(),
                    listagem.get(i).getStatus()
                });
            }
        }
    } catch (Exception e) {
        e.printStackTrace(); // Adicionando um print caso ocorra algum erro
    }
}
    
    
